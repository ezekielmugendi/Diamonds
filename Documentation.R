### This shiny app is developed from the diamonds dataset in ggplot2 which contains information about 54,000 diamonds, including the price, carat, color, clarity and cut of each diamond.

### The first plot shows the generated distribution of each variable in the diamonds dataset. 

### The second bar chart displays the total number of diamonds in the diamond dataset, grouped by cut. Each colored chart represents a combination of 'cut' and 'clarity'.